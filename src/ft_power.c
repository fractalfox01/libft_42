long	ft_power(long n)
{
	long	i;
	long	nbr;

	i = 1;
	nbr = 1;
	if (n == 1)
		return (1);
	if (n > 1)
	{
		while (i++ < n)
		{
			nbr *= 10;
		}
	}
	return (nbr);
}
