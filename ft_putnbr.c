#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		if (nb == -2147483648)
		{
			ft_putstr("-2147483648");
			return ;
		}
		ft_putchar('-');
		nb *= -1;
	}
	if ((nb / 10) > 0)
	{
		ft_putnbr((nb / 10));
	}
	ft_putchar((nb % 10) + 48);
}
