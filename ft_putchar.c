#include <unistd.h>
#include "libft.h"

void	ft_putchar(long c)
{
	if (c < 128)
		write(1, &c, 1);
	if (c > 127 && c <= 255)
		write(1, &c, 2);
	if (c > 255)
		write(1, &c, 3);
}
